package person.consciousness;

public class Knowledge {

    private int id;
    private int level;

    public Knowledge() { }

    public Knowledge(int level) {
        this.level = level;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }
}
